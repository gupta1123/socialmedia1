def normalize_format(value: str) -> str:
    return {"portrait": "4:5", "square": "1:1", "story": "9:16", "landscape": "16:9"}.get(value, value or "4:5")

def templates_for_job(content_job_id: str, output_format: str, supplied_templates: Any = None) -> List[Dict[str, Any]]:
    candidates = supplied_templates if isinstance(supplied_templates, list) and supplied_templates else list(VISUAL_TEMPLATES.values())
    eligible = []
    for template in candidates:
        if not isinstance(template, dict):
            continue
        jobs = template.get("content_jobs") if isinstance(template.get("content_jobs"), list) else [template.get("content_job_id")]
        formats = template.get("formats") if isinstance(template.get("formats"), list) else []
        if content_job_id not in jobs:
            continue
        if formats and output_format not in formats:
            continue
        eligible.append(deepcopy(template))
    return eligible or [deepcopy(t) for t in VISUAL_TEMPLATES.values() if t.get("content_job_id") == content_job_id]

def lever_options_for_job(content_job_id: str) -> Dict[str, Any]:
    return {"registry": deepcopy(LEVER_REGISTRY), "compatibility": deepcopy(LEVER_COMPATIBILITY.get(content_job_id, {})), "content_job": deepcopy(CONTENT_JOBS.get(content_job_id, {})), "variant_profiles": deepcopy(VARIANT_LEVER_PROFILES)}

def sanitize_creative_direction(direction: Dict[str, Any], fallback: Optional[Dict[str, Any]] = None) -> Dict[str, Any]:
    fallback = fallback or {}
    out = {}
    for key, allowed in LEVER_REGISTRY.items():
        value = direction.get(key, fallback.get(key))
        if key == "graphic_layer":
            values = value if isinstance(value, list) else ([value] if value else [])
            out[key] = [v for v in values if v in allowed]
        elif value in allowed:
            out[key] = value
        elif fallback.get(key) in allowed:
            out[key] = fallback[key]
        elif allowed:
            out[key] = allowed[0] if key != "graphic_layer" else []
    return out

def merge_variant_levers(base: Dict[str, Any], profile: Dict[str, Any]) -> Dict[str, Any]:
    merged = deepcopy(base or {})
    override = deepcopy((profile or {}).get("creative_direction") or (profile or {}).get("lever_overrides") or {})
    for key, value in override.items():
        merged[key] = value
    return sanitize_creative_direction(merged, base or {})

def prompt_facing_label(value: Any) -> str:
    text = str(value or "")
    return PROMPT_FACING_LEVER_LANGUAGE.get(text, text.replace("_", " "))

def describe_creative_direction(direction: Dict[str, Any]) -> str:
    parts = []
    for key in ["style_family", "hero_presentation", "layout_geometry", "type_voice", "text_architecture", "mood_mode", "density", "brand_visibility", "visual_mode"]:
        if direction.get(key):
            parts.append(f"{key.replace('_', ' ')}: {prompt_facing_label(direction.get(key))}")
    layers = direction.get("graphic_layer") or []
    if isinstance(layers, str):
        layers = [layers]
    if layers:
        parts.append("graphic details: " + ", ".join(prompt_facing_label(x) for x in layers[:4]))
    return "; ".join(parts)
